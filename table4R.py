def process_data(data):
    lines = data.strip().split('\n')
    cities = []
    values = []
    
    for line in lines:
        parts = line.split()
        # Βρίσκουμε τη θέση της τιμής (τελευταίο μέρος)
        value_part = parts[-1]
        # Η πόλη είναι όλα τα μέρη εκτός από τον α/α και την τιμή
        city_parts = parts[1:-1]
        
        # Αφαιρούμε τους αστερίσκους - και από ξεχωριστά tokens και από κολλημένα
        cleaned_city_parts = []
        for part in city_parts:
            # Αφαιρούμε αστερίσκους από την αρχή του κάθε μέρους
            if part.startswith('*'):
                part = part[1:]
            # Προσθέτουμε μόνο αν το μέρος δεν είναι κενό μετά την αφαίρεση
            if part:
                cleaned_city_parts.append(part)
        
        city = ' '.join(cleaned_city_parts)
        
        # Μετατροπή "ωϊ" σε "ωι"
        city = city.replace('ωϊ', 'ωι')
        
        value = value_part.replace(',', '.')
        
        cities.append(f'"{city}"')
        values.append(value)
    
    # Συνάρτηση για να προσθέτουμε αλλαγή γραμμής κάθε 10 στοιχεία
    def format_with_newlines(items, items_per_line=10):
        result = []
        for i in range(0, len(items), items_per_line):
            group = items[i:i + items_per_line]
            result.append(', '.join(group))
        return ',\n'.join(result)
    
    cities_str = format_with_newlines(cities)
    values_str = format_with_newlines(values)
    
    # Προσθήκη της σύνταξη ανάθεσης σε μεταβλητή
    dimoi_output = f"dimoi <- c({cities_str})"
    bathmologia_output = f"bathmologia <- c({values_str})"
    
    return dimoi_output, bathmologia_output

